# Function to check whether a binary tree is balanced or not.




from collections import deque
class Solution:
    def isBalanced(self, root):

        # add code here
        self.f = 1

        def solve(root):
            if root == None:
                return 0
            l = solve(root.left)
            r = solve(root.right)
            if abs(l-r) > 1:
                self.f = 0
            return max(l, r)+1

        solve(root)
        return self.f


